namespace Horseshoe.NET.Text
{
    /// <summary>
    /// A collection of text common text values for Horseshoe.NET
    /// </summary>
    public static class TextConstants
    {
        /// <summary>
        /// How to represent a null reference in most common scenarios
        /// </summary>
        public const string Null = "[null]";

        /// <summary>
        /// How to represent an empty <c>string</c> in most common scenarios
        /// </summary>
        public const string Empty = "[empty]";

        /// <summary>
        /// How to represent an empty <c>string</c> in most common scenarios
        /// </summary>
        public const string Whitespace = "[whitespace]";

        /// <summary>
        /// How to represent a simple space <c>char</c> in most common scenarios
        /// </summary>
        public const string Space = "[space]";

        /// <summary>
        /// How to represent a non-breaking space <c>char</c> in most common scenarios
        /// </summary>
        public const string NonBreakingSpace = "[nb-space]";

        /// <summary>
        /// How to represent a tab <c>char</c> in most common scenarios
        /// </summary>
        public const string HorizontalTab = "[tab]";

        /// <summary>
        /// How to represent a new line (carriage return) <c>char</c> in most common scenarios
        /// </summary>
        public const string CarriageReturn = "[cr]";

        /// <summary>
        /// How to represent a new line (line feed) <c>char</c> in most common scenarios
        /// </summary>
        public const string LineFeed = "[ln]";
    }
}
